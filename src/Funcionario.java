
public class Funcionario {
	private String nome;
	private double salario;
	private double cpf;
	
	public String getnome() {
		return nome;
		
	}
	
	public void setnome(String nome) {
		this.nome = nome;
		
	}
	
	public double getsalario() {
		return salario;
		
	}
	
	public void setsalario(double salario) {
		this.salario = salario;
		
	}

	public double getcpf() {
		return cpf;
		
	}
}
